"""encumbrance_mapper: тип обременения из ЕГРН → текст влияния на торги.

Закрывает гэп §10 SPEC (legal.encumbrances[].influence).

Источник типов — `object_restrictions.restrict_type` парсера. Формулировки
ориентированы на канонический язык отчётов оценщика и ЭТП-карточек.

Если тип не распознан — возвращается None, и Jinja-шаблон не печатает
строку «— ...» в перечислении обременений.
"""
from __future__ import annotations

from typing import Final


# Канонические тексты влияния. Ключ — нормализованный тип (lower-case).
# Множественные написания одного типа в БД отображаются на один ключ.
_INFLUENCE: Final[dict[str, str]] = {
    "ипотека":
        "не препятствует продаже; прекращается при исполнении условий торгов",
    "залог":
        "не препятствует продаже; прекращается при исполнении условий торгов",

    "аренда":
        "ограничивает использование на срок действия договора аренды",
    "договор аренды":
        "ограничивает использование на срок действия договора аренды",
    "безвозмездное пользование":
        "ограничивает использование на срок действия договора пользования",

    "сервитут":
        "ограниченное право пользования смежными лицами; сохраняется при смене собственника",
    "публичный сервитут":
        "публичное ограниченное право пользования; сохраняется при смене собственника",

    "арест":
        "препятствует переходу права; снимается после исполнения судебного акта",
    "запрет":
        "ограничивает регистрационные действия до снятия запрета",
    "запрещение":
        "ограничивает регистрационные действия до снятия запрещения",

    "доверительное управление":
        "право пользования закреплено за доверительным управляющим",

    # Зоны и охранные статусы из NSPD-обогащения
    "czuit_zone":
        "ограничения по виду использования согласно регламенту ЗОУИТ",
    "охранная зона":
        "ограничения по виду использования в пределах охранной зоны",
    "okn_territory":
        "объект охраны культурного наследия; согласование изменений с органом охраны",
    "водоохранная зона":
        "ограничения хозяйственной деятельности в водоохранной зоне",
    "санитарно-защитная зона":
        "ограничения размещения и хозяйственной деятельности в СЗЗ",
}


def map_encumbrance(restrict_type: str | None) -> str | None:
    """Вернуть текст влияния обременения на торги, либо None.

    Сопоставление case-insensitive, по подстроке (для случаев вроде
    «ипотека в силу закона» → ключ «ипотека»).
    """
    if not restrict_type:
        return None
    needle = restrict_type.strip().lower()
    if needle in _INFLUENCE:
        return _INFLUENCE[needle]
    # Substring match: «ипотека в силу закона» → «ипотека»
    for key, text in _INFLUENCE.items():
        if key in needle:
            return text
    return None


def known_types() -> tuple[str, ...]:
    """Список известных типов (для документации / валидации)."""
    return tuple(sorted(_INFLUENCE.keys()))
